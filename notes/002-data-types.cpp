/*
    Most used data types
*/

#include <iostream>
#include <string>

int main() {

    // Integer number
    int n1 = 3;

    // Double: floating-point number
    double n2 = 3.1;

    // Char: individual character. Requires single-quotes ('')
    char m1 = 'a';

    // String: sequence of characters. Requires <string> header
    std::string openSecret = "Bitcoin";

    // Boolean: true/false
    bool isRevolution = true;

    return 0;

}